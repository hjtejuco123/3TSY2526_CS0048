# Weather-based clothing recommendation Sample 13: Clothing recommendation based on temperature
temperature = 5
if temperature > 30:
    print("Wear light clothes, it's hot outside!")
elif temperature > 20:
    print("Perfect weather for a t-shirt")
elif temperature > 10:
    print("You might need a light jacket")
elif temperature > 0:
    print("Bundle up, it's cold!")
else:
    print("Stay indoors, it's freezing!")