# If-Elif-Else Statements
# Convert numeric score to letter grade Sample 11: Letter grade conversion
score = 101
if score >= 90:
    grade = "A"
elif score >= 80:
    grade = "B"
elif score >= 70:
    grade = "C"
elif score >= 60:
    grade = "D"
else:
    grade = "F"
print(f"Score: {score}, Grade: {grade}")